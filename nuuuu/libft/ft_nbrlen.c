#include "libft.h"

int		ft_nbrlen(int n)
{
	int	res;

	res = 0;
	if (n == 0)
		return (0);
	if (n < 0)
		res = 1;
	while (n != 0)
	{
		n = n / 10;
		++res;
	}
	return (res);
}
