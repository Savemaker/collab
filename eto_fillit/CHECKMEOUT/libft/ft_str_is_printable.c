#include "libft.h"

int	ft_str_is_printable(char *s)
{
	int i;

	i = 0;
	while (s[i] != '\0')
	{
		if (!(s[i] >= 32 && s[i] <= 126))
			return (0);
		i++;
	}
	return (1);
}
